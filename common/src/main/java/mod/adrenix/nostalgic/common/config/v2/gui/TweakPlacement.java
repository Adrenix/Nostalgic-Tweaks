package mod.adrenix.nostalgic.common.config.v2.gui;

/**
 * Private a specific placement position for a tweak in the configuration menu.
 * If the placement is TOP or BOTTOM then a position number is used from 0 to infinity to define placement.
 * If the placement is AUTO then tweaks will be lasted alphabetically.
 */

public enum TweakPlacement
{
    AUTO,
    TOP,
    BOTTOM
}
